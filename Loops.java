public class Loops {

      public static void main(String[] args) {
            /**
             * Do while loop syntax
             */

            int count = 0;
            do{
                  System.out.println("Hello worlds");
                  count++;

            }while(count < 3);

            //While loops
            System.out.println("results for while loop");
            count = 0;
            while (count < 0){
                  System.out.println("Hello worlds");
                  count++;
            }

            //For loop
            System.out.println("Result of for loops ");
            for (int i = 0 ; i < 3 ; i++){           
                                            //it will only print for predetermined number of times
                  System.out.println("Hello worlds");
            }



      }
      
}
